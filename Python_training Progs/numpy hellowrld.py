import numpy as np

# Create a 1-dimensional NumPy array from a Python list
my_array = np.array([1, 2, 3, 4, 5])

# Print the array
print("My array:", my_array)

# Print the type of the array
print("Type of my_array:", type(my_array))

# Print the shape of the array (number of elements)
print("Shape of my_array:", my_array.shape)